package app;

public class Planalto {
	
	private int largura;
	private int altura;
	
	public Planalto(int alt, int larg){
		this.altura = alt;
		this.largura = larg;
	}
	
	public int getLargura() {
		return largura;
	}
	public void setLargura(int largura) {
		this.largura = largura;
	}
	public int getAltura() {
		return altura;
	}
	public void setAltura(int altura) {
		this.altura = altura;
	}
	
	

}
